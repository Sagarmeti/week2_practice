class datatypes{
  public static void main(String[] args){

    //integer/numerical
    byte a=9;
    short b=90;
    int c=100;
    long d=10000;

    //decimal
    float f=2.0f;
    double dd=25.22;

    //character
    char cc='s';
    String ss="welcome to bitlabs";

    //boolean
    boolean t=true;

    System.out.println("Byte value is:" +a);
    System.out.println("short value is:" +b);
    System.out.println("Int value is:" +c);
    System.out.println("Long value is:" +d);

    System.out.println("float value is:" +f);
    System.out.println("double value is:" +dd);

    System.out.println("char value is:" +cc);
    System.out.println("String value is:" +ss);

    System.out.println("Boolean value is:" +t);
  }
}